
/**
 * add - adds two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of two integers
*/
int add(int a, int b)
{
  return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of two integers
*/

int sub(int a, int b)
{
  return (a - b);
}

/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product of two integers
*/

int mul(int a, int b)
{
  return (a * b);
}

/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient of two integers
*/

int div(int a, int b)
{
  return (a / b);
}

/**
 * mod - mod between two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder of two integers
*/

int mod(int a, int b)
{
  return (a % b);
}
